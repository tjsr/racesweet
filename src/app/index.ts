const configureChromiumCacheDirectory = (): void => {
  const baseDir = process.env.LOCALAPPDATA || process.env.TEMP;
  if (!baseDir) {
    return;
  }
  const cacheDir = path.join(baseDir, 'RaceSweet', 'chromium-cache');
  app.commandLine.appendSwitch('disk-cache-dir', cacheDir);
};

configureChromiumCacheDirectory();

process.on('warning', (warning) => {
  const trace = warning.stack ?? `${warning.name}: ${warning.message}`;
  console.warn(trace);
});

const configureSecurityHeaders = (): void => {
  const contentSecurityPolicy = buildContentSecurityPolicy();

  session.defaultSession.webRequest.onHeadersReceived((details, callback) => {
    let responseHeaders = details.responseHeaders ?? {};
    if (isAllowedCorsDownloadUrl(details.url)) {
      responseHeaders = injectCorsHeaders(responseHeaders);
    }

    if (details.resourceType === 'mainFrame') {
      responseHeaders = injectContentSecurityPolicyHeader(responseHeaders, contentSecurityPolicy);
    }

    callback({ responseHeaders });
  });
};

const showStartupError = (error: Error): void => {
  const detail = error.stack ?? error.message;

  console.error(`RaceSweet startup failed: ${detail}`);
  dialog.showErrorBox('RaceSweet startup failed', error.message);
};

const createWindow = async (): Promise<void> => {
  await waitForContentServer(MAIN_WINDOW_WEBPACK_ENTRY);

  configureSecurityHeaders();

  // Create the browser window.
  const mainWindow = new BrowserWindow({
    height: 600,
    show: false,
    webPreferences: {
      contextIsolation: false,
      nodeIntegration: true,
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
    },
    width: 1024,
  });

  await waitForWindowContentLoad(mainWindow, MAIN_WINDOW_WEBPACK_ENTRY);
  mainWindow.show();
};

// This method will be called when Electron has finished
// initialization and is ready to create browser windows.
// Some APIs can only be used after this event occurs.
app.on('ready', () => {
  createWindow().catch((error: Error) => {
    showStartupError(error);
    app.quit();
  });
});

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', (): void => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', (): void => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow().catch((error: Error) => {
      showStartupError(error);
      app.quit();
    });
  }
});

